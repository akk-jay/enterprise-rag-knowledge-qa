# ── Chinese font ────────────────────────────────────────────────────
FONT_CANDIDATES = [
    r"C:/Windows/Fonts/simhei.ttf",   # 黑体
    r"C:/Windows/Fonts/msyh.ttf",     # 微软雅黑
    r"C:/Windows/Fonts/simsun.ttc",   # 宋体
    r"C:/Windows/Fonts/simkai.ttf",   # 楷体
]
FONT_FILE = None
for path in FONT_CANDIDATES:
    if os.path.isfile(path):
        FONT_FILE = path
        break

if FONT_FILE is None:
    raise FileNotFoundError(
        "No Chinese TTF font found on this system. Tried:\n" +
        "\n".join(f"  {p}" for p in FONT_CANDIDATES)
    )


class ITSecurityPolicy(FPDF):
    """PDF generator for XX科技有限公司 IT安全管理制度."""

    def __init__(self):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_auto_page_break(True, margin=25)
        self.add_font("zh", "", FONT_FILE)
        self.add_font("zh", "B", FONT_FILE)

    # ── Header / Footer ─────────────────────────────────────────
    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("zh", "", 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 6, "XX科技有限公司 - IT安全管理制度 - 内部资料", align="L")
        self.cell(0, 6, "密级：内部公开", align="R", new_x="LMARGIN", new_y="NEXT")
        self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
        self.ln(4)

    def footer(self):
        if self.page_no() == 1:
            return
        self.set_y(-20)
        self.set_font("zh", "", 8)
        self.set_text_color(128, 128, 128)
        self.cell(0, 10, f"第 {self.page_no()} 页", align="C")

    # ── Cover page ──────────────────────────────────────────────
    def cover_page(self):
        self.add_page()
        # Top decorative bar
        self.set_fill_color(0, 80, 60)
        self.rect(0, 0, 210, 8, "F")
        self.ln(55)

        # Company name
        self.set_font("zh", "B", 28)
        self.set_text_color(0, 80, 60)
        self.cell(0, 14, "XX科技有限公司", align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(6)

        # Separator
        self.set_draw_color(0, 80, 60)
        self.set_line_width(0.6)
        x_center = self.w / 2
        self.line(x_center - 35, self.get_y(), x_center + 35, self.get_y())
        self.ln(10)

        # Document title
        self.set_font("zh", "B", 32)
        self.set_text_color(0, 80, 60)
        self.cell(0, 16, "IT 安全管理制度", align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(16)

        # Meta info
        self.set_font("zh", "", 12)
        self.set_text_color(80, 80, 80)
        meta_items = [
            ("版  本  号：", "V2.1"),
            ("生效日期：", "2025年3月1日"),
            ("编  制  部：", "信息技术部"),
            ("审  批  人：", "信息安全委员会"),
            ("密      级：", "内部公开"),
        ]
        for label, value in meta_items:
            self.cell(0, 10, f"{label}    {value}", align="C", new_x="LMARGIN", new_y="NEXT")
        self.ln(20)

        # Bottom bar
        self.set_fill_color(0, 80, 60)
        self.rect(0, 289, 210, 8, "F")

    # ── Section helpers ─────────────────────────────────────────
    def section_level1(self, title):
        self.ln(4)
        self.set_font("zh", "B", 13)
        self.set_text_color(0, 80, 60)
        self.multi_cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(0, 80, 60)
        self.set_line_width(0.3)
        y = self.get_y()
        self.line(self.l_margin, y, self.w - self.r_margin, y)
        self.ln(4)

    def section_level2(self, title):
        self.ln(2)
        self.set_font("zh", "B", 11)
        self.set_text_color(60, 60, 60)
        self.multi_cell(0, 7, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def body_text(self, text):
        self.set_font("zh", "", 10)
        self.set_text_color(40, 40, 40)
        self.multi_cell(0, 6.5, text, align="L", new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def body_bullet(self, text, indent=4):
        self.set_font("zh", "", 10)
        self.set_text_color(40, 40, 40)
        x0 = self.l_margin + indent
        self.set_x(x0)
        self.multi_cell(self.w - self.l_margin - self.r_margin - indent, 6.5,
                        text, align="L", new_x="LMARGIN", new_y="NEXT")


